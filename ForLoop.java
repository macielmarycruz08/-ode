class ForLoop // for loop, repetition 
{
	public static void main(String[] args)
	{
		//for ( start; stop; step)
		for (int i = 0; i <= 6; i+=1) // Input will be i = 0,1,2,3,4,5,6 
		{
			//System.out.println("Helloo"); 
			System.out.println("i = " + i); //Print i = 0 plus 1 till reaching 6
		} 
	}
}
